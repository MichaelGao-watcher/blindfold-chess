        function t(key) {
            const lang = localStorage.getItem('lang') || 'zh';
            return (i18n[lang] && i18n[lang][key]) || (i18n.en && i18n.en[key]) || key;
        }

        function updateTexts() {
            document.querySelectorAll('[data-i18n]').forEach(el => {
                const key = el.getAttribute('data-i18n');
                if (el.tagName === 'INPUT' && el.placeholder) {
                    el.placeholder = t(key);
                } else {
                    el.textContent = t(key);
                }
            });
            document.documentElement.lang = localStorage.getItem('lang') === 'en' ? 'en' : 'zh-CN';
            const langValue = document.getElementById('langValue');
            if (langValue) {
                const currentLang = localStorage.getItem('lang') || 'zh';
                langValue.textContent = currentLang === 'en' ? t('langZh') : t('langEn');
            }
            const themeLabel = document.getElementById('themeLabelText');
            if (themeLabel) {
                const currentTheme = document.documentElement.getAttribute('data-theme') || 'dark';
                themeLabel.textContent = currentTheme === 'dark' ? t('themeDark') : t('themeLight');
            }
        }

        // Theme logic
        (function() {
            const html = document.documentElement;
            const saved = localStorage.getItem('theme');
            if (saved) html.setAttribute('data-theme', saved);

            function updateThemeIcon(theme) {
                const sun = document.getElementById('panelIconSun');
                const moon = document.getElementById('panelIconMoon');
                if (sun && moon) {
                    if (theme === 'light') { sun.style.display = 'block'; moon.style.display = 'none'; }
                    else { sun.style.display = 'none'; moon.style.display = 'block'; }
                }
                const label = document.getElementById('themeLabelText');
                if (label) label.textContent = theme === 'dark' ? t('themeDark') : t('themeLight');
            }

            updateThemeIcon(saved || 'dark');

            document.getElementById('themeSetting').addEventListener('click', () => {
                const current = html.getAttribute('data-theme') || 'dark';
                const next = current === 'dark' ? 'light' : 'dark';
                html.setAttribute('data-theme', next);
                localStorage.setItem('theme', next);
                updateThemeIcon(next);
            });
        })();

        // Settings panel toggle
        (function() {
            const toggle = document.getElementById('settingsToggle');
            const panel = document.getElementById('settingsPanel');
            toggle.addEventListener('click', (e) => {
                e.stopPropagation();
                panel.classList.toggle('show');
            });
            document.addEventListener('click', (e) => {
                if (!panel.contains(e.target) && e.target !== toggle) {
                    panel.classList.remove('show');
                }
            });
        })();

        // Language switch
        document.getElementById('langSetting').addEventListener('click', () => {
            const current = localStorage.getItem('lang') || 'zh';
            const next = current === 'en' ? 'zh' : 'en';
            localStorage.setItem('lang', next);
            updateTexts();
        });
        function showDifficulty() {
            document.getElementById('startScreen').classList.remove('active');
            document.getElementById('startScreen').classList.add('hidden');
            const diff = document.getElementById('difficultyScreen');
            diff.classList.remove('hidden');
            requestAnimationFrame(() => diff.classList.add('active'));
        }

